def normalize_ui_language(value: Any) -> str:
    return current_language_to_ui_code(value)


def tr(language: str, key: str) -> str:
    clean_language = normalize_ui_language(language)
    return TRANSLATIONS.get(clean_language, TRANSLATIONS["es"]).get(
        key,
        TRANSLATIONS["es"].get(key, key),
    )
